#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	show_chars(int a, int b, int c)
{
	ft_putchar(a + '0');
	ft_putchar(b + '0');
	ft_putchar(c + '0');
	if (a != '7' )
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	while (a <= 7)
	{
		b = a + 1;
		while (b <= 8)
		{
			c = b + 1;
			while (c <= 9)
			{
				show_chars(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}
